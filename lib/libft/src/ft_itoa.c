#include "libft.h"

char	*ft_itoa(int n)
{
	char	*res;
	size_t	e;

	e = ft_nbrlen(n);
	if (!(res = (char *)malloc(e + 1)))
		return (NULL);
	res[e--] = '\0';
	if (n < 0)
	{
		res[0] = '-';
		if (n == -2147483648)
		{
			res[e--] = '8';
			n /= 10;
		}
		n *= -1;
	}
	res[e--] = n % 10 + '0';
	while (n /= 10)
		res[e--] = n % 10 + '0';
	return (res);
}
